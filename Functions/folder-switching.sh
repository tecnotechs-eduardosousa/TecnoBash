
# entrar no corporativo do sitag
sad() {
    print_info "Entrando na pasta corporativa do sitag..."

    if cd "$SITAG_ADAPT_PATH" 2>/dev/null; then
        clear
        print_success "Na pasta ${bold}${ciano}corporativa do sitag${reset}${verde}!${reset}"
    else
        print_error "Não foi possível entrar na pasta corporativa do sitag"
        sleep 0.7
        clear
    fi
}

